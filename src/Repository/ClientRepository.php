    public function getClientsPaginator(int $offset = 0, int $count = 100): Paginator
    {
        $query = $this->createQueryBuilder('c')
            ->orderBy('c.created_at', 'DESC')
            ->setMaxResults($count)
            ->setFirstResult($offset)
            ->getQuery()
        ;

        return new Paginator($query);
    }

